    private void findActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_findActionPerformed
        // TODO add your handling code here:
        if (choose.getSelectedIndex() == 1) {

            try {
                selectByNim(Integer.parseInt(in_nim.getText()));
            } catch (SQLException ex) {
                Logger.getLogger(AP_GUI.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else if (choose.getSelectedIndex() == 2) {
            selectByNama(in_nama.getText());
        } else if (choose.getSelectedIndex() == 3) {
            selectByAlamat(in_alamat.getText());
        } else if (choose.getSelectedIndex() == 4) {

            String jeniskelamin = "";
            if (lk.isSelected()) {
                jeniskelamin = "L";

            } else if (pr.isSelected()) {
                jeniskelamin = "P";
            }
            selectByjk(jeniskelamin);

        } else {
            try {
                table();
            } catch (SQLException ex) {

            }
        }
    }//GEN-LAST:event_findActionPerformed

