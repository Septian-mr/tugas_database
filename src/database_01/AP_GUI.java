    private void tabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMouseClicked
        // TODO add your handling code here:

        DefaultTableModel dtm = (DefaultTableModel) tabel.getModel(); //buat tabel
        int selectedRowIndex = tabel.getSelectedRow(); //ambil baris yang dipilih
        int id = (int) dtm.getValueAt(selectedRowIndex, 0); //dapatkan nilai data

        konek.koneksi();

        try {

            sql = "select * from list_mahasiswa where NIM like '%" + id + "%'";
            pst = (Statement) konek.con.prepareStatement(sql);
            ResultSet rs = pst.executeQuery(sql);

            while (rs.next()) { 
                in_nim.setText(rs.getString("Nim"));
                in_nama.setText(rs.getString("Nama"));
                in_alamat.setText(rs.getString("Alamat"));
                jenis = rs.getString("Gender");
                if (jenis.equals("L")) {
                    lk.setSelected(true);
                } else {
                    pr.setSelected(true);
                }

            }
        } catch (Exception ex) {
        }
    }//GEN-LAST:event_tabelMouseClicked

